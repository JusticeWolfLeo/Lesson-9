public class LogicSeasons {
    private static void main(String[] args) {
        int m = 10;
        if (m == 12){
            System.out.println("Зима");
        } else if (m == 1) {
            System.out.println("Зима");
        } else if (m == 2) {
            System.out.println("Зима");
        } else if (m >= 3 && m <= 5) {
            System.out.println("Весна");
        } else if (m >= 6 && m <= 8) {
            System.out.println("Лето");
        } else if (m >= 9 && m <= 11) {
            System.out.println("Осень");
        }
    }
}
